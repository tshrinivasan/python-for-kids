# we can do all math operations


age = 12

print( age + 10 )

a = 10
b = 5

print( a + b )
print( a - b )
print( a * b )
print( a / b )

# to do the math operations, all the components should be either int or float.

# if we have any string, we have to convert to int or float

age = "15"

print( age + 10 )

# the above will give error, as we can not add a string and integer. to fix this, convert the age to integer using int(age)

print( int(age) + 10 )


